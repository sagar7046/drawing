package com.google.firebase.codelab.awesomedrawingquiz.data;

import android.arch.persistence.room.Dao;
import android.arch.persistence.room.Insert;
import android.arch.persistence.room.OnConflictStrategy;
import android.arch.persistence.room.Query;

import java.util.List;

import io.reactivex.Single;

@Dao
public interface DrawingDao {

    @Insert(onConflict = OnConflictStrategy.REPLACE)
    void addAll(List<Drawing> drawings);

    @Query("SELECT * from drawings WHERE word NOT IN (:excludedWords) ORDER BY RANDOM() LIMIT 1")
    Single<Drawing> getRandomDrawings(List<String> excludedWords);
}
